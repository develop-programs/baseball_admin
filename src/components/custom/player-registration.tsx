// For states without specific districts in our example data, provide some defaults
const getDistricts = (state: string) => {
    return districtsByState[state] || ["District 1", "District 2", "District 3"];
};

const formSchema = z.object({
    region: z.string().min(1, {
        message: "Please select a region."
    }),
    state: z.string().min(1, {
        message: "Please select a state."
    }),
    district: z.string().min(1, {
        message: "Please select a district."
    }),
})

export function PlayerRegistration() {
    // Track available options based on previous selections
    const [availableStates, setAvailableStates] = useState<string[]>([]);
    const [availableDistricts, setAvailableDistricts] = useState<string[]>([]);

    // 1. Define your form.
    const form = useForm<z.infer<typeof formSchema>>({
        resolver: zodResolver(formSchema),
        defaultValues: {
            region: "",
            state: "",
            district: ""
        },
    })

    // Watch for region changes to update available states
    const selectedRegion = form.watch("region");
    useEffect(() => {
        if (selectedRegion) {
            setAvailableStates(statesByRegion[selectedRegion] || []);
            // Reset state and district when region changes
            form.setValue("state", "");
            form.setValue("district", "");
            setAvailableDistricts([]);
        } else {
            setAvailableStates([]);
        }
    }, [selectedRegion, form]);

    // Watch for state changes to update available districts
    const selectedState = form.watch("state");
    useEffect(() => {
        if (selectedState) {
            setAvailableDistricts(getDistricts(selectedState));
            // Reset district when state changes
            form.setValue("district", "");
        } else {
            setAvailableDistricts([]);
        }
    }, [selectedState, form]);

    // 2. Define a submit handler.
    function onSubmit(values: z.infer<typeof formSchema>) {
        // Do something with the form values.
        // ✅ This will be type-safe and validated.
        console.log(values)
    }

    return (
        <Form {...form}>
            <form onSubmit={form.handleSubmit(onSubmit)} className="grid gap-4">

                <FormField
                    control={form.control}
                    name="region"
                    render={({ field }) => (
                        <FormItem>
                            <FormLabel>Region</FormLabel>
                            <FormControl>
                                <Select
                                    onValueChange={field.onChange}
                                    defaultValue={field.value}
                                >
                                    <SelectTrigger className="w-full">
                                        <SelectValue placeholder="Select a region" />
                                    </SelectTrigger>
                                    <SelectContent>
                                        {indiaRegions.map((region) => (
                                            <SelectItem key={region} value={region}>
                                                {region}
                                            </SelectItem>
                                        ))}
                                    </SelectContent>
                                </Select>
                            </FormControl>
                            <FormMessage />
                        </FormItem>
                    )}
                />
                <FormField
                    control={form.control}
                    name="state"
                    render={({ field }) => (
                        <FormItem>
                            <FormLabel>State</FormLabel>
                            <FormControl>
                                <Select
                                    onValueChange={field.onChange}
                                    defaultValue={field.value}
                                    disabled={!selectedRegion}
                                >
                                    <SelectTrigger className="w-full">
                                        <SelectValue placeholder="Select a state" />
                                    </SelectTrigger>
                                    <SelectContent>
                                        {availableStates.map((state) => (
                                            <SelectItem key={state} value={state}>
                                                {state}
                                            </SelectItem>
                                        ))}
                                    </SelectContent>
                                </Select>
                            </FormControl>
                            <FormMessage />
                        </FormItem>
                    )}
                />
                <FormField
                    control={form.control}
                    name="district"
                    render={({ field }) => (
                        <FormItem>
                            <FormLabel>District</FormLabel>
                            <FormControl>
                                <Select
                                    onValueChange={field.onChange}
                                    defaultValue={field.value}
                                    disabled={!selectedState}
                                >
                                    <SelectTrigger className="w-full">
                                        <SelectValue placeholder="Select a district" />
                                    </SelectTrigger>
                                    <SelectContent>
                                        {availableDistricts.map((district) => (
                                            <SelectItem key={district} value={district}>
                                                {district}
                                            </SelectItem>
                                        ))}
                                    </SelectContent>
                                </Select>
                            </FormControl>
                            <FormMessage />
                        </FormItem>
                    )}
                />
                <Button type="submit" size="lg" className="bg-blue-500 hover:bg-blue-500/80">Continue <ArrowRightCircle /></Button>
            </form>
        </Form>
    )
}
